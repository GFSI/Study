//Sacar cuantos numeros positivos contiene el array
let index = 0;

function positiveNumbers(array) {
    for (number of array) {
    
        number >= 0 ? index++ : "";
    }
    return index
}

let result = positiveNumbers([5, -9, 65, -84, -743, -569, 532, 65, -963, 485, 740]);
